    newROI = dtiSetdiffROIs(roi2,roi1);
    name = {'Left-Cerebral-Cortex_V13mm_setdiff','Right-Cerebral-Cortex_V13mm_setdiff'};
    newROI.name = name{k};
    dtiWriteRoi(newROI, newROI.name,1)
    
